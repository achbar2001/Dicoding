/*Pada string juga kita dapat menggunakan operator plus (+). Operator tersebut pada string berfungsi 
untuk menggabungkan dua teks yang terpisah menjadi satu buah teks. Contohnya seperti ini:*/

let greet = "Hello";
let moreGreet = greet + greet;
console.log(moreGreet);

/* output: HelloHello */

const myName = "Luke";
console.log(`Hello, my name is ${myName}.`);

